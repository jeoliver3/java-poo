
package com.aluraoracle.cursojava.lacos;

// tabuada de 1 a 10

public class Tabuada {
    public static void main(String[] args) {
        
        for(int multi = 1; multi <=10; multi++)
        {
            for(int count = 0; count <=10; count++)
            {
                System.out.print(multi * count);
                System.out.print(" ");
            }
            System.out.println();
        }
        
        

        

    

}

        
    
}
